package com.sansriti.myapplication;

public class Colleague {
    private String name;
    private String contact;

    public Colleague() {}

    public Colleague(String name, String contact) {
        this.name = name;
        this.contact = contact;
    }

    public String getName() {
        return name;
    }

    public String getContact() {
        return contact;
    }
}
